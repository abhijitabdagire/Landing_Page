// first question div

let content_display = document.getElementById('Plus-icon');
let content_close = document.getElementById('cross-icon');

content_display.addEventListener ('click', () => {
    document.getElementById('show_1').style.display = "block";
    document.getElementById('cross-icon').style.display = "block";
    document.getElementById('Plus-icon').style.display = "none";
}
);
content_close.addEventListener ('click', () => {
    document.getElementById('show_1').style.display = "none";
    document.getElementById('cross-icon').style.display = "none";
    document.getElementById('Plus-icon').style.display = "block";
}
);

